# Regex para validar e normalizar MAC em qualquer formato comum.
_MAC_RE = re.compile(
    r"^[0-9A-Fa-f]{2}(?:[:-]?)([0-9A-Fa-f]{2})(?:[:-]?)"
    r"([0-9A-Fa-f]{2})(?:[:-]?)([0-9A-Fa-f]{2})(?:[:-]?)"
    r"([0-9A-Fa-f]{2})(?:[:-]?)([0-9A-Fa-f]{2})$"
)


class MacVendorService:
    """Lookup de fabricante via prefixo OUI do MAC address."""

    @staticmethod
    def normalize(mac: str) -> Optional[str]:
        """
        Normaliza um MAC address para o formato canonico ``XX:XX:XX:XX:XX:XX``.

        Suporta formatos:
        - ``XX:XX:XX:XX:XX:XX`` (Unix)
        - ``XX-XX-XX-XX-XX-XX`` (Windows)
        - ``XXXX.XXXX.XXXX`` (Cisco)
        - ``XXXXXXXXXXXX`` (sem separador)

        Returns:
            MAC normalizado (uppercase, com ':') ou None se invalido.
        """
        if not mac or not isinstance(mac, str):
            return None
        cleaned = re.sub(r"[^0-9A-Fa-f]", "", mac).upper()
        if len(cleaned) != 12:
            return None
        return ":".join(cleaned[i:i+2] for i in range(0, 12, 2))

    @classmethod
    def extract_oui(cls, mac: str) -> Optional[str]:
        """Extrai o prefixo OUI (3 primeiros bytes) do MAC normalizado."""
        normalized = cls.normalize(mac)
        if normalized is None:
            return None
        return normalized[:8]

    @classmethod
    def lookup(cls, mac: str) -> Dict[str, Any]:
        """
        Faz lookup do fabricante a partir do MAC address.

        Args:
            mac: MAC em qualquer formato comum.

        Returns:
            Dict com chaves:
              - valid (bool): True se o MAC tem formato valido.
              - normalized (str|None): MAC normalizado (XX:XX:XX:XX:XX:XX).
              - oui (str|None): prefixo OUI (XX:XX:XX).
              - vendor (str|None): nome do fabricante (None se nao encontrado).
              - source (str): "oui_table".
              - errors (list[str]): lista de erros de validacao.
        """
        result: Dict[str, Any] = {
            "valid": False,
            "normalized": None,
            "oui": None,
            "vendor": None,
            "source": "oui_table",
            "errors": [],
        }

        normalized = cls.normalize(mac)
        if normalized is None:
            result["errors"].append("invalid_format")
            return result

        oui = normalized[:8]
        result["valid"] = True
        result["normalized"] = normalized
        result["oui"] = oui
        result["vendor"] = _OUI_VENDORS.get(oui)
        if result["vendor"] is None:
            result["errors"].append("oui_not_in_table")
        return result

    @classmethod
    def known_vendor_count(cls) -> int:
        """Retorna a quantidade de OUIs mapeados na tabela."""
        return len(_OUI_VENDORS)
